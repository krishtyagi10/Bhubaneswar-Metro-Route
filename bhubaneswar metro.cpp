void shortPath(unordered_map<int, list<int> > adj, int source, int destination, string stations[55])
{
	unordered_map<int, bool> visited;
	unordered_map<int, int> parent;
	queue<int> q;
	q.push(source);
	visited[source] = true;
	parent[source] = -1;

	while (!q.empty())
	{
		int front = q.front();
		q.pop();

		for (int k : adj[front])
		{
			if (!visited[k])
			{
				visited[k] = true;
				parent[k] = front;
				q.push(k);
			}
		}
	}

	vector<int> ans;
	int currentNode = destination;
	ans.push_back(destination);

	while (currentNode != source)
	{
		currentNode = parent[currentNode];
		ans.push_back(currentNode);
	}

	reverse(ans.begin(), ans.end());
	for (int i = 0; i < ans.size(); i++)
	{
		int k = ans[i];
		cout << stations[k] << "->";
	}
}

